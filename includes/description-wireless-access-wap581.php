                            <h2>Características y Beneficios:</h2>
                            <ul>
                             <li>Compatibilidad con radio de doble banda concurrente de hasta 867 Mbps en una radio de 5.0 Ghz y 300 Mbps en una radio de 2.4 Ghz para aumentar la capacidad y la cobertura</li>
                             <li>2x2 MIMO con dos flujos espaciales en 5,0 GHz y 2,4 GHz permite el máximo rendimiento</li>
                             <li>La configuración de un solo punto, una tecnología sin controlador, simplifica la implementación y administración de múltiples puntos de acceso, sin necesidad de hardware adicional</li>
                             <li>Un conmutador LAN Gigabit Ethernet de 5 puertos admite dispositivos alimentados por PoE (PD) y equipos de fuente de alimentación (PSE) para que los dispositivos IP puedan alimentarse y el punto de acceso pueda recibir alimentación del conmutador de red troncal</li>
                             <li>La seguridad robusta, que incluye WPA2, 802.1X con autenticación segura RADIUS y detección de puntos de acceso no autorizados, ayudan a proteger la información comercial confidencial</li>
                             <li>El soporte del portal cautivo facilita el acceso de invitados altamente seguro y personalizado con múltiples derechos y roles</li>
                             <li>La instalación simple y la configuración y el asistente intuitivos basados ​​en la web facilitan una implementación y configuración rápidas y sencillas en minutos</li>
                             <li>La compatibilidad con PoE permite una fácil instalación sin costosos cableados adicionales</li>
                             <li>Un diseño elegante con varias antenas internas y un kit de montaje versátil que permite montarlo directamente en una caja de conexiones eléctrica o de datos</li>
                             <li>La calidad de servicio inteligente (QoS) prioriza el tráfico de red para ayudar a mantener las aplicaciones de red críticas funcionando al máximo rendimiento</li>
                             <li>El modo de suspensión de ahorro de energía y las funciones de control de puertos ayudan a aumentar la eficiencia energética</li>
                             <li>El modo Workgroup Bridge le permite expandir su red al conectarse de forma inalámbrica a una segunda red Ethernet</li>
                             <li>La compatibilidad con IPv6 le permite implementar futuras aplicaciones de red y sistemas operativos sin actualizaciones costosas</li>
                             <li>Una garantía de hardware limitada de por vida proporciona tranquilidad</li>
                             </ul>
