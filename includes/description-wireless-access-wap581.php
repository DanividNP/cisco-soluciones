                            <h3>Descripción del producto</h3>
                            <p align="justify">En el dinámico entorno empresarial actual, los empleados se vuelven más móviles y colaborativos que nunca. Para mantenerse productivos, necesitan un acceso confiable y de clase empresarial a las aplicaciones de red en toda la oficina. Los puntos de acceso de placa de pared de radio dual Cisco® WAP361 Wireless-AC/N con alimentación a través de Ethernet (PoE) brindan una manera simple y rentable de extender redes móviles altamente seguras y de alto rendimiento a sus empleados e invitados, para que puedan quedarse conectados en cualquier lugar de la oficina, independientemente de los dispositivos móviles que utilicen. Esta solución flexible le permite conectar a docenas de empleados y puede escalar para adaptarse a usuarios adicionales y necesidades comerciales cambiantes.</p>

                            <p align="justify">El punto de acceso WAP361 utiliza una radio de doble banda concurrente para mejorar la cobertura y la capacidad del usuario. La tecnología 2x2 de múltiples entradas y múltiples salidas (MIMO) con dos flujos espaciales permite que el punto de acceso funcione al máximo rendimiento en frecuencias de 5,0 GHz y 2,4 GHz. Las interfaces Gigabit Ethernet LAN con PoE facilitan una instalación flexible y pueden reducir los costos de cableado y cableado. Las funciones inteligentes de calidad de servicio (QoS) le permiten priorizar el tráfico sensible al ancho de banda para aplicaciones de video y voz sobre IP (VoIP).</p>

                            <p align="justify">Para brindar un acceso altamente seguro a visitantes y otros usuarios, el punto de acceso WAP361 Wireless-AC/N admite un portal cautivo con múltiples opciones de autenticación y la capacidad de configurar derechos, funciones y ancho de banda. Una página de inicio de sesión de invitado personalizada le permite presentar un mensaje de bienvenida y detalles de acceso, y refuerza su marca con los logotipos de la empresa.</p>

                            <p align="justify">Los puntos de acceso WAP361 son fáciles de configurar y usar, con una configuración intuitiva basada en un asistente para ponerlo en funcionamiento en minutos. Un diseño atractivo con opciones de montaje flexibles permite que los puntos de acceso se integren bien en cualquier entorno de pequeña empresa.</p>

                            <p align="justify">Para mejorar la confiabilidad y salvaguardar la información comercial confidencial, los puntos de acceso WAP361 admiten acceso protegido Wi-Fi (WPA) personal y empresarial, codificando todas sus transmisiones inalámbricas con un cifrado potente. Además, la autenticación 802.1X RADIUS ayuda a mantener alejados a los usuarios no autorizados.</p>

