            <h3>Características prominentes</h3>
            <p align="justify">Mayor rendimiento inalámbrico 
                <br>El punto de acceso Aironet 1815w es compatible con el último estándar 802.11ac Wave 2 para un mayor rendimiento, mayor acceso y redes de mayor densidad. Con radios duales simultáneas y banda dual con funcionalidad 802.11ac Wave 2 MU-MIMO, este punto de acceso puede manejar el creciente número de dispositivos de alto ancho de banda que pronto se convertirán en una parte común de la red.
                <br>Acceso por cable
                <br>El 1815w permite el acceso por cable a través de un solo puerto de detección automática RJ-45 10/100/1000. Admite modos de operación completos con alimentación PoE 802.3af. El 1815w viene con tres puertos Gigabit Ethernet locales, un puerto Gigabit Ethernet de enlace ascendente y un puerto RJ-45 de paso pasivo, lo que permite una variedad de conexiones.
                <br>Montaje
                <br>Este elegante punto de acceso con un factor de forma pequeño está diseñado con opciones de montaje flexibles en mente. Puede montarlo directamente en la pared o en numerosos estándares de unión de pared globales. El punto de acceso también es fácil de instalar.

