                                <tr>
                                    <td class="smallText"><strong><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">WiFi 6 (802.11ax)</font></font></strong></td>
                                    <td class="smallText"><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">El estándar emergente IEEE 802.11ax, también conocido como High-Efficiency Wireless (HEW) o Wi-Fi 6, se basa en 802.11ac. Ofrece una mejor experiencia en entornos típicos con un rendimiento más predecible para aplicaciones avanzadas como video 4K u 8K, aplicaciones de colaboración de alta definición y alta densidad, oficinas totalmente inalámbricas e IoT. Wi-Fi 6 está diseñado para usar las bandas de 2,4 GHz y 5 GHz, a diferencia del estándar 802.11ac.</font></font></td>
                                </tr>
                                <tr>
                                    <td><strong><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">OFDMA de enlace ascendente/descendente</font></font></strong></td>
                                    <td><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">La programación basada en acceso múltiple por división de frecuencia ortogonal (OFDMA) divide el ancho de banda en asignaciones de frecuencia más pequeñas denominadas unidades de recursos (RU), que se pueden asignar a clientes individuales en las direcciones de enlace descendente y ascendente para reducir la sobrecarga y la latencia.</font></font></td>
                                </tr>
                                <tr>
                                    <td><strong><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">Tecnología MU‑MIMO de enlace ascendente/descendente</font></font></strong></td>
                                    <td><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">&nbsp;Al admitir dos flujos espaciales, multiusuario de múltiples entradas y múltiples salidas (MU-MIMO) permite que los puntos de acceso dividan los flujos espaciales entre los dispositivos del cliente para maximizar el rendimiento.</font></font></td>
                                </tr>
                                
                                <tr>
                                    <td><strong><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">BSS para colorear</font></font></strong></td>
                                    <td><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">La reutilización espacial (también conocida como coloración del conjunto de servicios básicos [BSS]) permite que los puntos de acceso y sus clientes diferencien entre los BSS, lo que permite más transmisiones simultáneas.</font></font></td>
                                </tr>
                                
                                <tr>
                                    <td><strong><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">Hora de despertar objetivo</font></font></strong></td>
                                    <td><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">Un nuevo modo de ahorro de energía llamado Target Wake Time (TWT) permite que el cliente permanezca dormido y se despierte solo a horas preprogramadas (objetivo) para intercambiar datos con el punto de acceso. Esto ofrece importantes ahorros de energía para los dispositivos que funcionan con baterías, hasta tres o cuatro veces los ahorros logrados por 802.11n y 802.11ac.</font></font></td>
                                </tr>
                    
                                <tr>
                                    <td><strong><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">ASIC de radiofrecuencia de Cisco</font></font></strong></td>
                                    <td><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">Cisco RF ASIC es una radio definida por software (SDR) completamente integrada que puede realizar un análisis de espectro de RF avanzado y ofrece funciones como Cisco CleanAir, Wireless Intrusion Prevention System (wIPS), FastLocate y Dynamic Frequency Selection (DFS).</font></font></td>
                                </tr>
                                <tr>
                                    <td><strong><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">Bluetooth 5</font></font></strong></td>
                                    <td><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">La radio integrada Bluetooth Low Energy (BLE) 5 permite casos de uso basados ​​en la ubicación, como el seguimiento de activos, la orientación y el análisis.</font></font></td>
                                </tr>

                                <tr>
                                    <td class="smallText"><strong><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">Compatibilidad con Ethernet multigigabit</font></font></strong></td>
                                    <td class="smallText"><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">Proporciona velocidades de enlace ascendente de 2,5 G, 1 G y 100 M. Todas las velocidades son compatibles con cableado de Categoría 5e por primera vez en la industria, así como con cableado 10GBASE-T (IEEE 802.3bz).</font></font></td>
                                </tr>

