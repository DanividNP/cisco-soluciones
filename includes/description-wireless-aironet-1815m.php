            <h3>Características prominentes</h3>
            <p align="justify">Mayor rendimiento inalámbrico 
                <br>los puntos de acceso de la serie Aironet® 1815m son compatibles con el último estándar 802.11ac Wave 2 para un mayor rendimiento, mayor acceso y redes de mayor densidad. Con radios duales simultáneas y banda dual con funciones 802.11ac Wave 2 MU-MIMO, este punto de acceso puede manejar el creciente número de dispositivos de alto ancho de banda que pronto se convertirán en una parte común de la red.
                <br>Acceso por cable
                <br>la serie 1815m permite el acceso por cable a través de un solo puerto de detección automática RJ-45 10/100/1000. Admite modos de operación completos utilizando energía a través de Ethernet (PoE) 802.3af.
                <br>Montaje
                <br>estos puntos de acceso elegantes con un factor de forma pequeño están diseñados con opciones de montaje flexibles en mente, con soporte para colocar en techos o paredes.

