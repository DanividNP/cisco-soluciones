                                <h3>Descripción del producto</h3>

                                <p align="justify">Ideal para redes pequeñas y medianas, la serie Cisco® Aironet® 1830 ofrece un rendimiento inalámbrico líder en la industria con soporte para el último estándar de Wi-Fi, la nueva especificación 802.11ac Wave 2 de IEEE y cumple con los crecientes requisitos de las redes inalámbricas al ofrecer una mejor experiencia de usuario. La serie 1830 amplía el soporte a una nueva generación de clientes Wi-Fi, como teléfonos inteligentes, tabletas y computadoras portátiles de alto rendimiento que tienen soporte integrado 802.11ac Wave 1 o Wave 2.
                                
