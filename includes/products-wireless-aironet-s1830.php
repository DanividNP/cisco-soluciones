                         <div class="tab-pane" id="tab4">
                                <h2>Características y Beneficios:</h2>
                                <p align="justify">Con 802.11ac Wave 2, la serie 1830 proporciona una velocidad de datos de hasta 867 Mbps en la radio de 5 GHz, superando las velocidades de datos que ofrecen los puntos de acceso 802.11n de gama alta de la actualidad. También permite una tasa de datos total agregada de radio dual de hasta 1 Gbps, proporcionando la base necesaria para que las redes empresariales y de proveedores de servicios se mantengan a la vanguardia de las expectativas y necesidades de rendimiento y ancho de banda de sus usuarios inalámbricos.
                                <p align="justify">Debido a su conveniencia, el acceso inalámbrico es cada vez más la forma preferida de conectividad de red para los usuarios corporativos. Junto con este cambio, existe la expectativa de que la tecnología inalámbrica no debería ralentizar el trabajo diario de los usuarios, sino que debería permitir una experiencia de alto rendimiento al tiempo que permite a los usuarios moverse libremente. La serie 1830 ofrece un rendimiento líder en la industria para conexiones inalámbricas altamente seguras y confiables y proporciona una sólida experiencia de movilidad que incluye:
                                    <ul>
                                      <li>802.11ac Wave 2 con tecnología 3x3 de entrada múltiple y salida múltiple (MIMO) con dos flujos espaciales cuando se opera en modo MIMO de usuario único o multiusuario, que ofrece velocidades de 867 Mbps para una mayor capacidad y confiabilidad que los puntos de acceso de la competencia.</li>
                                      <li>MIMO multiusuario (MU-MIMO) permite la transmisión de datos a múltiples clientes compatibles con 802.11ac Wave 2 simultáneamente para mejorar la experiencia del cliente. Antes de MU-MIMO, los puntos de acceso 802.11n y 802.11ac Wave 1 podían transmitir datos a un solo cliente a la vez, generalmente denominado MIMO de usuario único.</li>
                                       <li>La tecnología de formación de haces de transmisión mejora el rendimiento del enlace descendente a los dispositivos móviles, incluidos los dispositivos de uno y dos flujos espaciales en 802.11ac, al tiempo que mejora la duración de la batería en dispositivos móviles como teléfonos inteligentes y tabletas.</li>
                                      <li>El modo de implementación flexible a través de Mobility Express Solution es ideal para implementaciones pequeñas y medianas que requieren varios puntos de acceso. La configuración sencilla permite que la serie 1830 se implemente en redes sin un controlador físico.</li>
                                </ul>

