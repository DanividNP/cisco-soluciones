                            <h3>Descripción del producto</h3>
                            <p align="justify">A medida que las aplicaciones comerciales se vuelven más potentes y sofisticadas, las organizaciones buscan nuevas formas de ampliar el rendimiento y el alcance de las redes de sus oficinas. Ofrecer conectividad inalámbrica segura y de alta velocidad a empleados, socios e invitados en cualquier lugar de la oficina es clave. El punto de acceso de banda seleccionable Cisco WAP321 Wireless-N con configuración de punto único facilita la entrega de redes inalámbricas 802.11n avanzadas con características de clase empresarial, a un precio asequible. Esta solución flexible es perfecta para conectar hasta 20 empleados en un solo WAP321. Con la configuración de punto único, la LAN inalámbrica puede escalar hasta un total de ocho dispositivos WAP321 para brindar una cobertura más amplia y admitir usuarios adicionales a medida que cambian las necesidades comerciales.</p>

                            <p align="justify">Diseñado específicamente para pequeñas empresas, el Cisco WAP321 ofrece tecnología inalámbrica 802.11n de banda seleccionable para brindar un alto rendimiento y un rango extendido en toda su oficina. Las funciones avanzadas de calidad de servicio (QoS) le permiten priorizar el tráfico para admitir aplicaciones sensibles al ancho de banda. Este control sofisticado le permite aprovechar la voz sobre WLAN (VoWLAN) para realizar o recibir llamadas a través de la infraestructura LAN inalámbrica.</p>

                            <p align="justify">Diseñado para organizaciones en crecimiento, el Cisco WAP321 ofrece la capacidad de escalar sin problemas su red conectando LAN cableadas de forma inalámbrica para reducir los costos de cableado e instalación. Admite múltiples identificadores de conjuntos de servicios (SSID) para permitirle segregar el tráfico para diferentes departamentos, usuarios y dispositivos de comunicación.</p>

                            <p align="justify">Para las empresas que necesitan proporcionar acceso inalámbrico seguro para invitados, Cisco WAP321 ofrece soporte de portal cautivo que le permite crear un punto de acceso inalámbrico con autenticación de visitantes.</p>

                            <p align="justify">El Cisco WAP321 es fácil de configurar y usar, con una configuración intuitiva basada en un asistente para que pueda comenzar a utilizarlo en minutos. Un diseño elegante y compacto con opciones de montaje flexibles permite que el punto de acceso se integre sin problemas en cualquier entorno de pequeña empresa. La compatibilidad con alimentación a través de Ethernet (PoE) hace que el dispositivo sea fácil de instalar sin necesidad de enchufes de alimentación separados o cableado nuevo y costoso.</p>

                            <p align="justify">Para mejorar la confiabilidad y salvaguardar los datos confidenciales de la información comercial, Cisco WAP321 es compatible con el acceso protegido WiFi (WPA2), codificando todas sus transmisiones inalámbricas con un potente cifrado. La autenticación RADIUS 802.1X ayuda a mantener alejados a los usuarios no autorizados. Single Point Setup es una tecnología de implementación de múltiples puntos de acceso sin controlador que le permite implementar y administrar múltiples puntos de acceso de una manera simple y rentable en todas sus instalaciones.</p>

                            <p align="justify">La configuración de punto único proporciona un método centralizado único para administrar y controlar los servicios inalámbricos en varios dispositivos. A través de un punto de acceso en la LAN, tiene una vista única de toda la WLAN para replicar la configuración, la seguridad y la administración en todos los puntos de acceso.</p>

                            <p align="justify">Con Cisco WAP321, puede aprovechar su red existente para brindar acceso inalámbrico de alto rendimiento con la escalabilidad que necesita a medida que su negocio crece y evoluciona.</p>
                            
