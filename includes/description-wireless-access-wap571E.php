                            <ul>
                              <li>Las redes inalámbricas 802.11n altamente seguras y de alta velocidad ofrecen un rendimiento mejorado y un rango extendido para aplicaciones que requieren un uso intensivo del ancho de banda.</li>
                              <li>La instalación y configuración basadas en asistente permiten una implementación rápida y sencilla.</li>
                              <li>La tecnología de gestión e implementación de puntos de acceso múltiples sin controlador de Single Point Setup simplifica la configuración.</li>
                              <li>La interfaz LAN Gigabit Ethernet proporciona conectividad de alta velocidad para descargas más rápidas y aplicaciones exigentes.</li>
                              <li>El acceso inalámbrico de banda seleccionable le permite cambiar de banda para minimizar la interferencia de otros dispositivos de oficina y mejorar el rendimiento.</li>
                              <li>La seguridad robusta, que incluye WPA2, 802.1X con autenticación segura RADIUS y detección de puntos de acceso no autorizados, ayuda a proteger la información comercial confidencial.</li>
                              <li>La compatibilidad con PoE permite una fácil instalación sin el gasto de cableado adicional.</li>
