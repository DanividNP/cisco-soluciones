                            <h2>Descripción general:</h2>
                            <p align="justify">Hoy en día, las redes seguras son imperativas para todas las empresas, incluso las más pequeñas.</p>
                            <p align="justify">El router de seguridad de la serie RV340 proporciona a los usuarios comerciales conectividad avanzada y acceso seguro de alta velocidad para la creciente demanda y uso de servicios digitales.</p>
                            <p align="justify">Los routers Cisco de la serie RV340 conectan a las pequeñas empresas de forma segura a Internet. Los routers protegen a los empleados de amenazas como virus, ataques a la red, contenido no deseado y sitios web maliciosos. Todo eso sin comprometer la experiencia online.</p>
                            <p align="justify">La implementación de funciones avanzadas de Unified Threat Management (UTM) en una plataforma de router de alto rendimiento crea una de las soluciones de conectividad para pequeñas empresas más deseables.</p>
                            <p align="justify">Los estándar son puertos WAN duales, firewall de clase empresarial y opciones de VPN flexibles. Wi-Fi-5 integrado, conmutador LAN de 16 puertos y puertos LAN PoE están disponibles en variantes de modelo.</p>
                            <p align="justify">Para la seguridad basada en la nube, se encuentra disponible una integración de Cisco Umbrella ™ fácil de configurar.</p>
                            <h3>Seguridad</h3>
                            <p align="justify">La seguridad de TI es una prioridad para empresas de todo tipo y tamaño. Dado que el tamaño de una oficina o una oficina remota suele ser bastante pequeño, es menos probable que el personal de TI esté disponible y ofrezca soporte al usuario. Cualquier entorno de oficina requiere la misma funcionalidad que la oficina corporativa en términos de seguridad, que puede incluir firewall, VPN, IPS, aplicaciones de bloqueo y protección web.</p>
                            <p align="justify">La serie 340 de Cisco ofrece seguridad integral y el mejor rendimiento de su clase.</p>
                            <ul>
                                <li>Cortafuegos: con estado, escalable y flexible.</li>
                                <li>Funciones UTM (su elección de local o en la nube): las funciones de seguridad unificadas brindan conectividad a Internet confiable y altamente segura con detección y políticas de aplicaciones en la nube.</li>
                                <li>Conectividad segura: funcionalidad VPN flexible compatible con Cisco AnyConnect® Secure Mobility Client.</li>
                                <li>Rendimiento acelerado por hardware: arquitectura basada en ARM de doble núcleo con VPN integrada acelerada por hardware.</li>
                                <li>Rendimiento acelerado por hardware: arquitectura basada en ARM de doble núcleo con VPN integrada acelerada por hardware.</li>
                                <li>Gestión de red de panel único centrada en el socio o el cliente con la integración en FindIT Manager o la interfaz de usuario web.</li>
                                <li>Actualizaciones de software automatizadas para mantener el firmware actualizado.</li>
                            </ul>
                            <p align="justify">Las funciones de seguridad incluyen un conjunto de funciones UTM integrado y una integración de Cisco Umbrella basada en la nube. Estas funciones se pueden utilizar de forma independiente entre sí y requieren una licencia de software. Cuando se activa la integración de Cisco Umbrella, la seguridad web integrada se desactiva. Todas las demás funciones de seguridad funcionarán.</p>
                            <p align="justify">Cisco Umbrella es un servicio de seguridad web basado en la nube que ofrece protección automática contra sitios web maliciosos o comprometidos, phishing, devoluciones de llamada C2 y malware. Con Cisco Umbrella, obtiene visibilidad y cumplimiento en la capa de DNS, por lo que puede bloquear solicitudes a dominios e IP maliciosos antes de que se establezca una conexión a su red o puntos finales. Y debido a que está integrado en la base de Internet y se entrega desde la nube, Umbrella es el producto de seguridad más simple de implementar y brinda una protección poderosa y efectiva. La integración del enrutador permite una configuración sencilla, una mejor visibilidad y generación de informes, y una licencia de sucursal apta para pequeñas empresas para Cisco Umbrella.</p>
                            <p align="justify">Las características de seguridad UTM integradas incluyen: prevención de intrusiones, antivirus de puerta de enlace, identificación de aplicaciones, identificación de clientes, seguridad web integrada. Estas características protegen su red de los ataques de Internet y, al mismo tiempo, permiten establecer políticas para el uso corporativo de Internet al limitar la navegación por Internet a las categorías de sitios adecuadas y eliminar el tráfico de red no deseado, lo que aumenta la productividad de los empleados y el rendimiento general de la red.</p>
