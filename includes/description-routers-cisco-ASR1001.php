                                <h2>Reinvención del router perimetral</h2>
                                <p align="justify">Los routers de servicios de agregación Cisco ASR serie 1000 agregan múltiples conexiones WAN y servicios de red, incluido el cifrado y la administración del tráfico, y los reenvían a través de conexiones WAN a velocidades de línea de 2.5 a 200 Gbps. Los enrutadores contienen redundancia de hardware y software en un diseño de alta disponibilidad líder en la industria.
                                    <br><br>La última incorporación a la familia Cisco ASR es el enrutador Cisco ASR 1001-X, un enrutador de una sola unidad de rack (RU) que admite una capacidad de reenvío de 2,5 a 20 Gbps. Las velocidades del enrutador Cisco ASR 1001-X se pueden "aumentar" gradualmente hasta 20 Gbps con una licencia de actualización de rendimiento simple, en lugar de tener que comprar blades de hardware adicionales o nuevos routers.
                                    <br><br>La serie Cisco ASR 1000 es compatible con el software Cisco IOS XE, un sistema operativo modular con empaquetado modular, velocidad de funciones y capacidad de recuperación poderosa. Los procesadores de servicios integrados (ESP) Cisco ASR de la serie 1000, que se basan en la tecnología del procesador Cisco QuantumFlow, aceleran muchas funciones avanzadas, como la seguridad de acceso basada en criptografía; Traducción de direcciones de red (NAT), defensa de subprocesos con Firewall basado en zonas de Cisco (ZBFW), inspección profunda de paquetes (DPI), Elemento de borde unificado de Cisco (CUBE) y un conjunto diverso de funciones de interconexión de centros de datos (DCI). Estos servicios se implementan en el software Cisco IOS XE sin la necesidad de soporte de hardware adicional.
                                    <br><br>Los routers Cisco ASR 1000 se ubican en el borde de su centro de datos empresarial u oficina grande que se conecta a la WAN, así como en los puntos de presencia (POP) de los proveedores de servicios. La serie Cisco ASR 1000 beneficiará a los siguientes tipos de clientes:
                                </p>
                                <ul>
                                    <li>Empresas que experimentan un tráfico de red explosivo a medida que aumentan la movilidad, las redes en la nube y el uso de video y colaboración. Los ASR de Cisco consolidan estos distintos flujos de tráfico y les aplican propiedades de administración y redundancia del tráfico para mantener un rendimiento constante entre los sitios empresariales y las ubicaciones de la nube.</li>
                                    <li>Proveedores de servicios de red que necesitan brindar servicios de alto rendimiento, como DCI y agregación de servidores de sucursales, a clientes comerciales. Los proveedores de servicios también pueden utilizar los enrutadores multiservicio para implementar servicios gestionados y alojados en servicios empresariales y multimedia para clientes residenciales.</li>
                                    <li>Clientes existentes de Cisco 7200 Series Router (fin de venta) que buscan una migración simple a una nueva plataforma multiservicio que ofrezca un mayor rendimiento con el mismo diseño.</li>
                                </ul>
